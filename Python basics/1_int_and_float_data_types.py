# Studying int and float data types
# int - integers. float - floating-point numbers
# the main functions applicable to int and float:

#addition
print(1+2)
#resualt: 3

#subtraction
print(3-5)
#resualt: -2

#division
print(4/2)
#resualt: 2.0
#division
print(7/2)
#resualt: 3.5
#whatever numbers are divisible, there will always be a float data type

#adding int and float outputs float
print(2.0+3)
#resualt: 5.0

#multiplication integers
print(2*5)
#resualt: 10

#multiplication floating-point number and integer
print(1.2*3)
#resualt: 3.5999999999999996

#'round' function rounds numbers to the specified number of digits
print(round(1.2*3, 2))
#resualt: 3.6

#remainder division
print(3%2)
#resualt: 1

#parity check
print(6%2==0)
#resualt: True (bull type)

#parity check
print(7%2==0)
#resualt: False (bull type)

#multiplication of several numbers
print(2*2*2*2)
#resualt: 16

#degree conversion
print(2**4)
#resualt: 16

#priority of multiplication over addition
print(2+2*2)
#resualt: 6

#addition and multiplication using parentheses
print((2+2)*2)
#resualt: 8

